function ChangeColorLight(){
    let bodyEl = document.querySelector('body')
    let navEl = document.querySelector('.navbar')
    let navtext = document.querySelector('.nav-brand')
    let offcans = document.querySelector('.offcanvas')
    let navlink = document.querySelector('a')
    let cardin = document.querySelectorAll('.card')
    let but = document.querySelectorAll('button')
    let h_1 = document.querySelector('h1')
    bodyEl.classList.remove('text-bg-dark')
    bodyEl.classList.add('text-bg-light')
    navEl.classList.remove('bg-body-tertiary')
    navEl.classList.add('navbar-dark')
    navEl.classList.add('bg-dark')
    navtext.classList.remove('text-bg-light')
    navtext.classList.add('text-bg-dark')
    navlink.classList.remove('link-dark')
    navlink.classList.add('link-light')
    but.classList.remove('btn-light')
    but.classList.add('btn-dark')
    h_1.classList.remove('text-light')
    h_1.classList.add('text-dark')
    cardin.classList.remove('text-bg-dark')
    cardin.classList.add('border-dark')
}

function ChangeColorDark(){
    let bodyEl = document.querySelector('body')
    let navEl = document.querySelector('.navbar')
    let navtext = document.querySelector('.nav-item')
    let offcans = document.querySelector('.offcanvas')
    let navlink = document.querySelectorAll('.nav-link')
    let cardin = document.querySelectorAll('.card')
    let but = document.querySelectorAll('button')
    let h_1 = document.querySelectorAll('h1')
    bodyEl.classList.remove('text-bg-light')
    bodyEl.classList.add('text-bg-dark')
    navEl.classList.remove('navbar-dark')
    navEl.classList.remove('bg-dark')
    navEl.classList.add('bg-body-tertiary')
    navtext.classList.remove('text-bg-dark')
    navtext.classList.add('text-bg-light')
    offcans.classList.remove('text-bg-dark')
    navlink.classList.remove('link-light')
    navlink.classList.add('link-dark')
    but.classList.remove('btn-dark')
    but.classList.add('btn-light')
    h_1.classList.remove('text-dark')
    h_1.classList.add('text-light')
    cardin.classList.add('text-bg-dark')
    cardin.classList.remove('border-dark')
    cardin.classList.add('border-light')
}

firstButton = document.querySelector('#light')
firstButton.addEventListener('click', ChangeColorLight)

secondButton = document.querySelector('#dark')
secondButton.addEventListener('click', ChangeColorDark)